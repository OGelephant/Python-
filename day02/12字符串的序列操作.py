# 空格也算一个字符
s1 = "hello hao"


# 拿到的是字符
print(s1[6])
print(s1[0])
print(s1[-1])

# 切片操作： 字符串[开始索引：结束索引:step=1] 顾头不顾尾
# 拿到的是字符串
print(s1[6:])
# 缺省就代表取到最后
# 要注意切片的方向,只要切片方向不一致，就会得到空

print(s1[:-4:-1])

# 翻转字符串
print(s1[::-1])


s2 = "你好，"
s3 = "世界"

print(s2+s3)

print("烫"*80)

# 计算字符串长度：元组的个数 解释器内置函数 len
s4 = "hello world"

# 针对容器类型，用in判断某个程序是否存在

print("hello" in s4)
